namespace Vido
{
    /// <summary>
    /// Defines the possible speed levels for animations.
    /// </summary>
    public enum AnimationSpeed
    {
        /// <summary>
        /// No animation should be displayed.
        /// </summary>
        None,
        /// <summary>
        /// The animation speed should be fast.
        /// </summary>
        Fast,
        /// <summary>
        /// The animation speed should be average.
        /// </summary>
        Medium,
        /// <summary>
        /// The animation speed should be slow.
        /// </summary>
        Slow
    }
}
